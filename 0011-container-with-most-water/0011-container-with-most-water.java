class Solution {
    public int maxArea(int[] height) {
        int maxCap = 0;
        int start = 0;
        int end = height.length - 1;
        while(start < end){
            int h = Math.min(height[start],height[end]);
            int width = end - start ;
            int currCap = h*width;

            if(height[start] < height[end]){
                start++;
            }else{
                end--;
            }
            maxCap = Math.max(currCap,maxCap);
        }
        return maxCap;
    }
}